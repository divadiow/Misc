/****************************************************************************
 * Name:  update_events_timestamp
 * Description:
 * @brief       update the UTC time of the event
 * @param[out]   pEvents: array of events
 * @param[in]   count: length of event array
 ****************************************************************************/
static void update_events_timestamp(sEvent *pEvents, int count)
{
    int i;

    for (i = 0; i < count; i++) {
        if (NULL == (&pEvents[i])) {
            Log_e("null event pointer");
            return;
        }
#ifdef EVENT_TIMESTAMP_USED
        pEvents[i].timestamp = HAL_Timer_current_sec();  /* should be UTC and accurate */
#else
        pEvents[i].timestamp = 0;
#endif
    }
}

